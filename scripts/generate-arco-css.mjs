import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const ARCO_STYLE_JS = path.join(
  root,
  "node_modules/@arco-design/mobile-react/esm/style.js",
);
const OUT_CSS = path.join(root, "src/app/arco.css");
function uniq(arr) {
  return [...new Set(arr)];
}
async function main() {
  const src = await fs.readFile(ARCO_STYLE_JS, "utf8");
  const lines = src
    .split(/\r?\n/g)
    .map((l) => l.trim())
    .filter(Boolean);
  const imports = [];
  for (const line of lines) {
    const m = /^import\s+['"]([^'"]+)['"]\s*;?$/.exec(line);
    if (!m) continue;
    imports.push(m[1]);
  }
  // Map Arco's LESS entry imports to their prebuilt CSS outputs.
  // - ../style/public.less -> @arco-design/mobile-react/style/css/public.css
  // - ./xxx/style -> @arco-design/mobile-react/esm/xxx/style/css/index.css
  const cssImports = [];
  for (const spec of imports) {
    if (spec === "../style/public.less") {
      cssImports.push("@arco-design/mobile-react/style/css/public.css");
      continue;
    }
    const styleMatch = /^\.\/([^/]+)\/style$/.exec(spec);
    if (styleMatch) {
      const comp = styleMatch[1];
      cssImports.push(`@arco-design/mobile-react/esm/${comp}/style/css/index.css`);
    }
  }
  const out = `/* Auto-generated by scripts/generate-arco-css.mjs\n * Source: @arco-design/mobile-react/esm/style.js\n */\n\n${uniq(cssImports)
    .map((p) => `@import "${p}";`)
    .join("\n")}\n`;
  await fs.mkdir(path.dirname(OUT_CSS), { recursive: true });
  await fs.writeFile(OUT_CSS, out, "utf8");
  // eslint-disable-next-line no-console
  console.log(`Generated ${path.relative(root, OUT_CSS)} (${cssImports.length} imports)`);
}
main().catch((err) => {
  // eslint-disable-next-line no-console
  console.error(err);
  process.exit(1);
});

